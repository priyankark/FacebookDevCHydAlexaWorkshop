// This sample demonstrates handling intents from an Alexa skill using the Alexa Skills Kit SDK (v2).
// Please visit https://alexa.design/cookbook for additional examples on implementing slots, dialog management,
// session persistence, api calls, and more.
const Alexa = require('ask-sdk-core');

const characterGuide = {
    "elsa": "Elsa is the perfect mythic character magical and larger than life.",
    "anna": "Anna is the perfect fairytale character; unflappable, she is the forever optimist.",
    "kristoff": "Kristoff is a true outdoorsman. He lives high up in the mountains where he harvests ice and sells it to the kingdom of Arendelle.",
    "olaf": "He's Olaf and he likes warm hugs. He is by far the friendliest snowman to walk the mountains above Arendelle."
}

const LaunchRequestHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'LaunchRequest';
    },
    handle(handlerInput) {
        const speakOutput = 'Welcome! You can ask me about your favorite characters from the movie Frozen! Who would you like to know about? ';
        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .getResponse();
    }
};

const CharacterGuideIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'CharacterIntent';
    },
    handle(handlerInput) {
       let characterName = Alexa.getSlotValue(handlerInput.requestEnvelope, 'characterName').toLowerCase();
       let description = characterGuide[characterName];
       const speakOutput = characterName in characterGuide ?
                           `Here is what I know about ${characterName}. ${description}`
                           : `Oops, I found no info about ${characterName}`;
       return handlerInput.responseBuilder
              .speak(speakOutput)
              .getResponse();
    }
};



